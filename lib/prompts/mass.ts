type CardInfo = { nameZh: string; name: string; isReversed: boolean };

export function buildMassUserPrompt(
  theme: MassTheme,
  groupNumber: number,
  groupSymbol: string,
  question: string,
  verificationCards: CardInfo[],
  readingCards: CardInfo[]
): string {
  const themeLabel = THEME_LABELS[theme];

  const verifyList = verificationCards
    .map((c, i) => `第${i + 1}张：${c.nameZh}（${c.name}）— ${c.isReversed ? "逆位" : "正位"}`)
    .join("\n");

  const readList = readingCards
    .map((c, i) => `第${i + 1}张：${c.nameZh}（${c.name}）— ${c.isReversed ? "逆位" : "正位"}`)
    .join("\n");

  return `这是一个大众占卜，今天的主题是「${themeLabel}」，博主的问题是：「${question}」

现在是第${groupNumber}组（参照物：${groupSymbol}）。

【验证牌 · 3张】用于描述星座 / 当下状态 / 特定讯息：
${verifyList}

【解读牌 · 5张】针对「${question}」进行深度解析：
${readList}

请严格按照规定的五个章节结构输出完整解读。前三个章节（星座/状态/特定讯息）基于验证牌，后两个章节（牌的解读/结语）基于解读牌。让选了这组的每一个宝宝都感觉"说的就是我"。`;
}
