export function streamMockReading(
  onChunk: (chunk: string) => void,
  onDone: () => void
): () => void {
  const chunks = MOCK_TAROT_READING.split(" ");
  let i = 0;
  const interval = setInterval(() => {
    if (i >= chunks.length) {
      clearInterval(interval);
      onDone();
      return;
    }
    onChunk(chunks[i] + " ");
    i++;
  }, 30);
  return () => clearInterval(interval);
}
